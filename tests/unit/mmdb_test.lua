-- Tiny harness
----------------------------------------------------------------------

local _passes, _fails = 0, 0
local function eq( what, got, want )
    if got == want then _passes = _passes + 1
    else
        _fails = _fails + 1
        io.stderr:write( string.format( "FAIL: %s\n  got:  %s\n  want: %s\n",
            what, tostring( got ), tostring( want ) ) )
    end
end
local function truthy( what, v )
    if v then _passes = _passes + 1
    else _fails = _fails + 1; io.stderr:write( "FAIL: " .. what .. " got=" .. tostring( v ) .. "\n" ) end
end
local function falsy( what, v )
    if not v then _passes = _passes + 1
    else _fails = _fails + 1; io.stderr:write( "FAIL: " .. what .. " got=" .. tostring( v ) .. "\n" ) end
end
local function near( what, got, want )
    if type( got ) == "number" and math.abs( got - want ) < 1e-9 then _passes = _passes + 1
    else
        _fails = _fails + 1
        io.stderr:write( string.format( "FAIL: %s\n  got:  %s\n  want: ~%s\n",
            what, tostring( got ), tostring( want ) ) )
    end
end
local function arr_eq( what, got, want )
    if type( got ) ~= "table" then
        _fails = _fails + 1
        io.stderr:write( "FAIL: " .. what .. " not a table (got " .. tostring( got ) .. ")\n" )
        return
    end
    if #got ~= #want then
        _fails = _fails + 1
        io.stderr:write( string.format( "FAIL: %s length got=%d want=%d\n", what, #got, #want ) )
        return
    end
    for i = 1, #want do
        if got[ i ] ~= want[ i ] then
            _fails = _fails + 1
            io.stderr:write( string.format( "FAIL: %s[%d] got=%s want=%s\n",
                what, i, tostring( got[ i ] ), tostring( want[ i ] ) ) )
            return
        end
    end
    _passes = _passes + 1
end

local function must_open( name )
    local r, err = mmdb.open( FIXDIR .. name )
    assert( r, "open " .. name .. " failed: " .. tostring( err ) )
    return r
end

----------------------------------------------------------------------
-- open(): error returns (bad arg / missing / non-MMDB)
----------------------------------------------------------------------

do
    local r, err = mmdb.open( nil )
    falsy( "open(nil) returns nil", r ); truthy( "open(nil) has err", err )

    r, err = mmdb.open( "" )
    falsy( "open('') returns nil", r ); truthy( "open('') has err", err )

    r, err = mmdb.open( FIXDIR .. "does-not-exist.mmdb" )
    falsy( "open(missing) returns nil", r ); truthy( "open(missing) has err", err )

    -- README.md contains the ASCII "MaxMind.com" but NOT the binary
    -- \xab\xcd\xef marker prefix, so it must be rejected cleanly.
    r, err = mmdb.open( FIXDIR .. "README.md" )
    falsy( "open(non-MMDB) returns nil", r )
    truthy( "open(non-MMDB) has err", err )
end

