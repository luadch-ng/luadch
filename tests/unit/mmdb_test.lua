--[[

    tests/unit/mmdb_test.lua

    Unit tests for core/mmdb.lua (#78 Phase D1). Validated against
    MaxMind's canonical test databases in fixtures/mmdb/ (produced by
    MaxMind's own mmdbwriter, not by our reader) with expected lookup
    values generated by the reference `maxminddb` Python reader v3.1.1.
    This keeps the validation non-circular: a bug shared between our
    encoder and decoder is impossible because we ship no encoder.

    Coverage:
      - open(): bad-arg / missing-file / non-MMDB error returns
      - metadata parse for all three record sizes (24 / 28 / 32)
      - full data-type decode (decoder.mmdb: map, array, uint16/32/64,
        uint128, int32, string, bytes, double, float, boolean, nested
        map, pointer-dedup)
      - IPv4-native tree traversal + clean miss (ipv4-28)
      - IPv6 traversal + v4-in-v6 embedding (ipv6-32, country)
      - realistic Country schema extraction (country.iso_code + names)
      - v6-in-v4-db rejection, closed-reader rejection
      - dot-call + colon-call ergonomics

    Run from the repo root:
      lua5.4 tests/unit/mmdb_test.lua

]]--

local FIXDIR = "tests/unit/fixtures/mmdb/"

----------------------------------------------------------------------
-- use() shim: core/mmdb.lua + core/ipmatch.lua run under init.lua's
-- restricted env in the hub. Here we hand them the real stdlib. `io`
-- is the REAL io because the reader opens actual fixture files.
----------------------------------------------------------------------

local _real = {
    type = type, tonumber = tonumber, tostring = tostring, error = error,
    pcall = pcall, setmetatable = setmetatable, string = string,
    table = table, io = io, ipairs = ipairs,
}
local _loaded_ipmatch
local function use_stub( name )
    if name == "ipmatch" then return _loaded_ipmatch end
    local v = _real[ name ]
    if v ~= nil then return v end
    error( "mmdb_test shim: missing dep " .. tostring( name ) )
end

_G.use = use_stub
_loaded_ipmatch = assert( loadfile( "core/ipmatch.lua" ) )( )
local mmdb = assert( loadfile( "core/mmdb.lua" ) )( )

