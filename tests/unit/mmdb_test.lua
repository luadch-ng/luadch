do
    local dec = must_open( "MaxMind-DB-test-decoder.mmdb" )
    eq( "decoder ip_version",  dec.metadata.ip_version, 6 )
    eq( "decoder record_size", dec.metadata.record_size, 24 )
    eq( "decoder node_count",  dec.metadata.node_count, 426 )
    eq( "decoder db_type",     dec.metadata.database_type, "MaxMind DB Decoder Test" )

    local v4 = must_open( "MaxMind-DB-test-ipv4-28.mmdb" )
    eq( "ipv4-28 ip_version",  v4.metadata.ip_version, 4 )
    eq( "ipv4-28 record_size", v4.metadata.record_size, 28 )

    local v6 = must_open( "MaxMind-DB-test-ipv6-32.mmdb" )
    eq( "ipv6-32 ip_version",  v6.metadata.ip_version, 6 )
    eq( "ipv6-32 record_size", v6.metadata.record_size, 32 )

    local co = must_open( "GeoLite2-Country-Test.mmdb" )
    eq( "country ip_version",  co.metadata.ip_version, 6 )
    eq( "country record_size", co.metadata.record_size, 28 )
    eq( "country db_type",     co.metadata.database_type, "GeoLite2-Country" )
end

----------------------------------------------------------------------
-- Full data-type decode (decoder.mmdb). Golden values from the
-- reference maxminddb reader.
----------------------------------------------------------------------

do
    local r = must_open( "MaxMind-DB-test-decoder.mmdb" )
    local rec = r:lookup( "1.1.1.0" )
    truthy( "decoder 1.1.1.0 hit", rec )
    if rec then
        arr_eq( "decoder array",   rec.array, { 1, 2, 3 } )
        eq( "decoder boolean",     rec.boolean, true )
        eq( "decoder bytes len",   rec.bytes and #rec.bytes, 4 )
        eq( "decoder bytes value", rec.bytes, "\0\0\0\42" )
        near( "decoder double",    rec.double, 42.123456 )
        near( "decoder float",     rec.float, 1.100000023841858 )
        eq( "decoder int32",       rec.int32, -268435456 )
        eq( "decoder uint16",      rec.uint16, 100 )
        eq( "decoder uint32",      rec.uint32, 268435456 )
        eq( "decoder uint64",      rec.uint64, 1152921504606846976 )
        eq( "decoder uint128 hex", rec.uint128, "1" .. string.rep( "0", 30 ) )   -- 2^120
        eq( "decoder utf8_string", rec.utf8_string, "unicode! \xe2\x98\xaf - \xe2\x99\xab" )
        truthy( "decoder map",     rec.map )
        if rec.map then
            truthy( "decoder map.mapX", rec.map.mapX )
            if rec.map.mapX then
                arr_eq( "decoder map.mapX.arrayX", rec.map.mapX.arrayX, { 7, 8, 9 } )
                eq( "decoder map.mapX.utf8_stringX", rec.map.mapX.utf8_stringX, "hello" )
            end
        end
    end

    -- All-zero / empty variant.
    local z = r:lookup( "0.0.0.0" )
    truthy( "decoder 0.0.0.0 hit", z )
    if z then
        eq( "decoder empty array len",  z.array and #z.array, 0 )
        eq( "decoder empty boolean",    z.boolean, false )
        eq( "decoder empty bytes",      z.bytes, "" )
        eq( "decoder empty uint16",     z.uint16, 0 )
        eq( "decoder empty uint32",     z.uint32, 0 )
        eq( "decoder empty int32",      z.int32, 0 )
        eq( "decoder empty uint128",    z.uint128, "0" )
        eq( "decoder empty utf8_string", z.utf8_string, "" )
        truthy( "decoder empty map is table", type( z.map ) == "table" )
    end

    -- v4-in-v6 embedding is exercised by the plain "1.1.1.0" lookup
    -- above: this DB is ip_version 6, so a v4 lookup already traverses
    -- the ::/96 subtree via mmdb's ZERO12 prefix branch (see also the
    -- country-DB v4 hits below). We do NOT assert the equivalent v6
    -- literal "::1.1.1.0" here because ipmatch only parses the
    -- v4-mapped form "::ffff:a.b.c.d", not the deprecated
    -- v4-compatible "::a.b.c.d" (RFC 4291 2.5.5.1) - real clients never
    -- present the latter, so it is not an mmdb concern.
end

----------------------------------------------------------------------
-- IPv4-native traversal + 28-bit records + clean miss (ipv4-28)
----------------------------------------------------------------------

do
    local r = must_open( "MaxMind-DB-test-ipv4-28.mmdb" )
    local a = r:lookup( "1.1.1.1" );  eq( "ipv4-28 1.1.1.1", a and a.ip, "1.1.1.1" )
    local b = r:lookup( "1.1.1.2" );  eq( "ipv4-28 1.1.1.2", b and b.ip, "1.1.1.2" )
    local c = r:lookup( "1.1.1.3" );  eq( "ipv4-28 1.1.1.3 (/31 shares .2)", c and c.ip, "1.1.1.2" )
    local d = r:lookup( "1.1.1.16" ); eq( "ipv4-28 1.1.1.16", d and d.ip, "1.1.1.16" )

    local miss, err = r:lookup( "2.2.2.2" )
    falsy( "ipv4-28 2.2.2.2 clean miss (nil)", miss )
    falsy( "ipv4-28 2.2.2.2 no error on miss", err )

    falsy( "ipv4-28 255.255.255.255 miss", r:lookup( "255.255.255.255" ) )

    -- v6 lookup against a v4-only DB is an error, not a miss.
    local v6r, v6err = r:lookup( "::1" )
    falsy( "ipv4-28 v6 lookup returns nil", v6r )
    truthy( "ipv4-28 v6 lookup returns err", v6err )

    -- dot-call ergonomics.
    local dot = r.lookup( "1.1.1.1" ); eq( "ipv4-28 dot-call", dot and dot.ip, "1.1.1.1" )
end

----------------------------------------------------------------------
-- IPv6 traversal + 32-bit records + v4-in-v6 miss path (ipv6-32)
----------------------------------------------------------------------

do
    local r = must_open( "MaxMind-DB-test-ipv6-32.mmdb" )
    local a = r:lookup( "::1:ffff:ffff" ); eq( "ipv6-32 ::1:ffff:ffff", a and a.ip, "::1:ffff:ffff" )
    local b = r:lookup( "::2:0:1" );       eq( "ipv6-32 ::2:0:1 (/122 net ::2:0:0)", b and b.ip, "::2:0:0" )

    -- v4-in-v6 traversal reaches the ::/96 subtree; this DB has no v4
    -- data there, so a clean miss (and, critically, no crash in the
    -- start-node embedding).
    local m4, e4 = r:lookup( "1.1.1.1" )
    falsy( "ipv6-32 v4-in-v6 clean miss", m4 )
    falsy( "ipv6-32 v4-in-v6 no error", e4 )

    falsy( "ipv6-32 ::1:0:0 miss", r:lookup( "::1:0:0" ) )
end

----------------------------------------------------------------------
-- Realistic Country schema + v4-in-v6 hits (GeoLite2-Country-Test)
----------------------------------------------------------------------

do
    local r = must_open( "GeoLite2-Country-Test.mmdb" )

    local gb = r:lookup( "2.125.160.216" )
    truthy( "country 2.125.160.216 hit", gb )
    if gb then
        eq( "country GB iso_code",   gb.country and gb.country.iso_code, "GB" )
        eq( "country GB name en",    gb.country and gb.country.names and gb.country.names.en, "United Kingdom" )
        eq( "country GB continent",  gb.continent and gb.continent.code, "EU" )
        eq( "country GB reg_country", gb.registered_country and gb.registered_country.iso_code, "FR" )
    end

    -- IPv4 lookups against the v6 tree (the common GeoIP case).
    local se = r:lookup( "89.160.20.128" )
    local us_v4 = r:lookup( "81.2.69.160" )
    local jp = r:lookup( "2001:218::1" )
    eq( "country 81.2.69.160 (v4) GB",   us_v4 and us_v4.country.iso_code, "GB" )
    eq( "country 89.160.20.128 (v4) SE", se and se.country.iso_code, "SE" )
    eq( "country 2001:218::1 (v6) JP",   jp and jp.country.iso_code, "JP" )

    falsy( "country 10.0.0.1 miss",  r:lookup( "10.0.0.1" ) )
    falsy( "country 214.0.0.0 miss", r:lookup( "214.0.0.0" ) )
end

